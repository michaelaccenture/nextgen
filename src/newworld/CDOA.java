package newworld;

public class CDOA {
	private String sex;
	private int age;
	
	public CDOA(String sex, int age) {
		super();
		this.sex = sex;
		this.age = age;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
}
